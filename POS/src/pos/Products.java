    private void reportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportBtnActionPerformed
        // TODO add your handling code here:
        String report = "Product.pdf";

        Document document = new Document(PageSize.A5.rotate());

        try {
            PdfWriter.getInstance(document, new FileOutputStream(report));
        } catch (FileNotFoundException ex) {

        } catch (DocumentException ex) {
            Logger.getLogger(Products.class.getName()).log(Level.SEVERE, null, ex);
        }
        document.open();
        PdfPTable table = new PdfPTable(9);
        cell = new PdfPCell();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(dataconn, username, password);
            Statement st = conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            String query = "Select * from product";
            rs = st.executeQuery(query);
            table.addCell("product_id");
            table.addCell("produce_code");
            table.addCell("product_name");
            table.addCell("unit_id");
            table.addCell("category_id");
            table.addCell("unit_in_stock");
            table.addCell("unit_price");
            table.addCell("discount_percentage");
            table.addCell("user_id");

            cell.setFixedHeight(72f);
            while (rs.next()) {
                table.addCell(rs.getString("product_id"));
                table.addCell(rs.getString("produce_code"));
                table.addCell(rs.getString("product_name"));
                table.addCell(rs.getString("unit_id"));
                table.addCell(rs.getString("category_id"));
                table.addCell(rs.getString("unit_in_stock"));
                table.addCell(rs.getString("unit_price"));
                table.addCell(rs.getString("discount_percentage"));
                table.addCell(rs.getString("user_id"));
            }
            document.add(table);
            document.close();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Products.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Products.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(Products.class.getName()).log(Level.SEVERE, null, ex);
        }
            if (Desktop.isDesktopSupported()){
            try{
                // Open a file on the Desktop

                File myfile = new File("Product.pdf");
                Desktop.getDesktop().open(myfile);
            }
            catch(Exception ex){

            }
            // Step 5 Closing the document
            document.close();
            }
    }//GEN-LAST:event_reportBtnActionPerformed

