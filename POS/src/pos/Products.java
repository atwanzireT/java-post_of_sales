    public void CategoryCombofill() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(dataconn, username, password);
            Statement stat = conn.createStatement();
            String comboQuery = "Select category_id, category_name from productcategory";
            rs = stat.executeQuery(comboQuery);
            while (rs.next()) {
                CategoryIDCombo.addItem(Integer.toString(rs.getInt("category_id")));
                categoryCombo.addItem(rs.getString("category_name"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

